class Switch
{
	public static void main(String args[])
	{
		int field=5;
		switch(field)
		{
			case 1:
			System.out.println("computer engineering");
			break;
		
			case 2:
			System.out.println("mechanical engineering");
			break;
		
			case 3:
			System.out.println("IT engineering");
			break;
			case 4:
			System.out.println("ENTC engineering");
			break;
		
			case 5:
			System.out.println("ELECTRONICS engineering");
			break;
		
			case 6:
			System.out.println("CIVIL engineering");
			break;
			default:
				System.out.println("no engineering");
			break;
			
		}
	}
}
		
		
			